-- Simple SELECT
SELECT *
FROM CUSTOMER
WHERE C_NATION LIKE 'ALGERIA';

-- query for explain the plan of the query
EXPLAIN PLAN FOR SELECT *
                 FROM CUSTOMER
                 WHERE C_NATION LIKE 'ALGERIA';

-- Show The Plan
SELECT PLAN_TABLE_OUTPUT
FROM TABLE (DBMS_XPLAN.DISPLAY());

-- Index Creation
CREATE INDEX hiIndex
  ON CUSTOMER (C_NATION);

-- select using Index
