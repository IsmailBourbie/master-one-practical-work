FROM CUSTOMER
WHERE C_NATION LIKE 'ALGERIA';

EXPLAIN PLAN For SELECT /*+ INDEX(CUSTOMER hiIndex) */ *
FROM CUSTOMER
WHERE C_NATION LIKE 'ALGERIA';


